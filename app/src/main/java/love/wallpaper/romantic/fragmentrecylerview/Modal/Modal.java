package love.wallpaper.romantic.fragmentrecylerview.Modal;

public class Modal {
    private String Title;
    private String Des;

    public Modal(String title, String des) {
        Title = title;
        Des = des;
    }

    public String getTitle() {
        return Title;
    }

    public void setTitle(String title) {
        Title = title;
    }

    public String getDes() {
        return Des;
    }

    public void setDes(String des) {
        Des = des;
    }
}
